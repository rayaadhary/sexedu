          <h2>KENAPA SEX EDUCATION ITU PENTING? </h2>
          <br></br>
          <br>Sebenarnya dalam bentuk apa sih sex education disampaikan di sekolah kita? Apakah disisipkan dalam sesi
          pelajaran khusus cewek yaitu tentang kesehatan wanita seperti seputar menstruasi (haid), penggunaan pembalut,
          kesehatan Miss V. Atau disisipkan di pelajaran kesehatan lainnya yang membahas organ reproduksi dan pubertas? </br>
          <br>Di beberapa sekolah, materi sex education kadang juga kadang disisipkan di mata pelajaran Pendidikan
          Jasmani dan Kesehatan (Penjaskes), Agama, Bimbingan Konseling, atau bahkan diberikan oleh pihak luar seperti
          lembaga dan organisasi yang datang ke sekolah.</br>
          <br>Dikutip dari CNN Indonesia, menurut Direktur Pendidikan Dasar dan Menengah Kementrian Pendidikan dan Kebudayaan
          Hamid Muhammad bilang kalo sex education emang nggak secara langsung disebut di kurikulum, namun secara eksplisit
          udah masuk dalam pendidikan kesehatan reproduksi. Menurutnya, selama pihak sekolah bisa mengajarkan materi pendidikan
          kesehatan reproduksi sesuai dengan aturan, para pelajar bisa memahami soal seksualitas.</br>
          <br>Yang jadi jadi pertanyaan, adalah apakah materi yang hanya disampaikan dua tiga kali bisa membuat kita paham
          dan mengerti secara mendalam tentang apa itu seks? Sementara sudah jelas seks nggak hanya melulu tentang fisik tapi justru lebih
          luas dari pada itu.</br>
