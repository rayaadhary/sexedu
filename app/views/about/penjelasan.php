          <br>
          <h5>SEKS ITU NGGAK CUMA TENTANG (KONTAK) FISIK TAPI JUGA EMOSIONAL</h5>
          <br>Sex education di Indonesia sayangnya nggak sesuai dengan realitas perilaku seksual remaja saat ini. Pendidikan seks yang
          ada sekarang terlalu menitikberatkan sama fungsi organ reproduksi tapi melupakan sisi emosional. Masa remaja adalah masa
          dimana emosi kita masih belum stabil, banyak kejadian remaja melakukan hubungan pra-nikah karena terbawa emosi dan kasih
          sayang tanpa tahu dan sadar resikonya. Walaupun penting buat kita tahu fungsi organ-organ tubuh dan gimana menjaga
          kesehatannya, tapi penting juga buat kita paham kalo hubungan seks bukan cuma sekedar demi memiliki keturunan tapi
          juga berhubungan dengan perasaan, hubungan kita dengan lawan jenis, dan tanggung jawab. Mirisnya, perasaan ini jugalah
          yang nggak jarang ngejebak kita buat melakukan kontak fisik dan hubungan pra-nikah atas dasar cinta, sekedar pembuktian.
          Apakah hal ini dibahas? Sayangnya dari apa yang Hapifyourworld temui, mereka berharap remaja dapat mengetahuinya sendiri.</br>
          </br>
          <h5>GIMANA CARA KITA MENGENDALIKAN NAFSU</h5>
          <br>Sex education yang diberikan oleh sekolah cenderung lebih menekankan bahaya dan resiko seks pra-nikah dari sudut
          pandang moral dan agama. Dan terkadang terkesan menghakimi daripada membimbing. Kita hanya tahu kalo seks pra-nikah
          bisa nyebabin kehamilan yang nggak diinginkan, penyakit menular seksual, dan tentunya dosa. Tapi kita jarang ngedapetin
          bimbingan ilmu tentang gimana caranya kita bisa mengendalikan nafsu yang bisa mengarah ke tindakan seks pra-nikah.
          Karena terlalu fokus sama pencegahan, sekolah, orang tua dan juga lembaga yang terkait melupakan satu point penting
          yaitu gimana dengan anak-anak remaja yang sudah aktif secara seksual? Apakah ada pendidikan khusus atau wadah dimana
          mereka bisa berdiskusi atau mengadu secara pribadi? Kalo nggak ada, lalu kemana mereka harus mencari solusi?
          Selain itu, dikutip dari IDNTimes, memblokir situs ‘berbahaya’ atau melarang remaja A, B, C,... nggak akan memecahkan masalah,
          karena tidak akan mengubah kenyataan kalo manusia emang pada dasarnya punya nafsu. Jadi kenapa malah nggak diajarkan cara untuk
          mengendalikannya?</br>
          <br> </br>
          <h5>OUR PERSONAL BOUNDARIES DAN CARANYA UNTUK BILANG “NGGAK”</h5>
          <br>Salah satu materi yang kurang diberikan ke siswa remaja juga adalah soal kekerasan dalam pacaran. Kekerasan
          dalam pacaran itu nggak cuma dalam bentuk fisik aja lho, tapi juga kekerasan verbal, emosional, bahkan seksual.
          Udah banyak survei dan studi yang membuktikan kalo korban kekerasaan dalam pacaran mengalami kontak seksual yang
          sebenarnya nggak mereka inginkan. Dari yang diraba bagian tubuhnya, bahkan sampai dipaksa untuk berhubungan seks
          pra-nikah oleh pacarnya sendiri. Mirisnya, musti diakui kalo seks pra-nikah cenderung menghakimi perempuan daripada
          laki-laki.</br>
          <br>Kasus seperti ini sebenarnya berhubungan banget dengan sex education dan personal boundaries serta gimana cara
          kita berani buat bilang “nggak”. Nggak sedikit lembaga ataupun sekolah yang melewati hal ini dan malah melarang
          siswanya buat berpacaran, bukannya memberikan pemahaman gimana cara berpacaran yang sehat dan aman.</br>
          <br></br>
          <h5>JADI GIMANA SIH SEBENARNYA SEX EDUCATION YANG IDEAL?</h5>
          <h8>Berdasarkan dari tiga point di atas, sex education yang seperti apa sih yang seharusnya diajarkan ke kita?</h8>
          <br></br>
          <br>Yang pertama, kita harus memahami dulu kalo dorongan seks itu adalah naluriah. Hasrat untuk melakukan seks itu
          nggak aneh sama sekali kok, karena naluri ini emang pemberian dari Tuhan. Tapi kita sebagai manusia juga diberi
          akal dimana kita punya kuasa penuh untuk mengontrolnya demi untuk kebaikan kita sendiri. Bukan karena takut karena
          omongan orang, apalagi paksaan.</br>
          <br>Yang kedua, mengendalikan nafsu itu susah emang tapi bukan berarti nggak bisa. Mengendalikan nafsu bukan cuma
          sekedar melarang, tapi juga demi mencegah terjadinya hal-hal yang nggak diinginkan karena kita belum siap menanggung
          resiko dan tanggung jawabnya. Ada banyak cara untuk mengendalikan nafsu, misalnya meminimalisir kontak fisik sama pacar,
          jangan nonton adegan yang mengarah atau berpotensi nimbulin gairah, atau kalo udah mulai mengarah “ke sana” buru-buru
          alihkan perhatian kita ke hal lain. Ingat, kita punya kuasa penuh sama pikiran dan badan kita.</br>
          <br>Yang ketiga, seks nggak cuma dilakukan demi memperoleh keturunan tapi juga karena kedua orang sama-sama saling
          mencintai. Pacaran emang ngelibatin emosi dan perasaan, tapi itu bukan excused untuk dijadikan alasan sebagai pembuktian
          cinta atau buat nunjukkin rasa sayang kita ke pacar. Kalo pacar kita benar-benar sayang sama kita, dia pasti akan
          me-respect dan menjaga kita sebaik-baiknya. Jadi kalo misalnya pacar kita malah melakukan hal sebaliknya, mungkin
          harus dipertimbangkan apakah pacar kita suka atau hanya nafsu aja. Don’t hesitate to say “NO”, badanmu adalah hak
          milikmu sepenuhnya. Dan jangan juga kemakan omongan temen, hanya karena teman-teman kita udah melakukannya bukan
          berarti kita juga harus ngelakuin. Cara terbaik keluar dari peer pressure kayak gini adalah cari teman lain yang
          bisa bawa dampak positif dan nge-support kita secara real.</br>
          <br></br>
          <h5>SELAIN ITU ADA HAL LAIN LAGI YANG MUSTI DISERTAKAN DI SEX EDUCATION,</h5>
          <br>Sex education sebaiknya nggak cuma mencegah tapi juga memberikan solusi dan menanggulanginya. Kita nggak bisa
          cuek dan nutup mata kalo pada kenyataannya ada banyak remaja yang udah terlanjur melakukan seks pra-nikah, dan
          nggak sedikit juga yang akhirnya melakukan hal ekstrim seperti aborsi. Orangtua, guru ataupun orang dewasa lainnya
          bisa mengajarkan dan mengingatkan kita, tapi tetap aja mereka nggak bisa sepenuhnya mengawasi dan mengendalikan
          tindakan kita kan, jadi semua keputusan ada di tangan kita. Makanya informasi kayak alat kontrasepsi sebenarnya
          penting banget buat disosialisasiin (dengan catatan kalo alat tersebut nggak 100 persen melindungi kita), dan
          anjuran apa yang harus kita lakukan apabila kita terlanjur melakukan seks pra-nikah. Misalnya menyediakan wadah
          untuk mereka melakukan konsultasi pribadi, ataupun adanya psikiater yang khusus untuk remaja. Dengan adanya
          wadah seperti ini maka edukasi dan informasi yang diterima oleh remaja juga bisa tersaring dengan baik dan
          nggak ada namanya judging dengan stigma-stigma tertentu.</br>
          <br>Akan lebih baik kalo sex education bukan lagi hal tabu dan semua orang berpikiran terbuka untuk membicarakannya,
          serta menginformasikannya dengan benar ke para remaja.</br>
